//Types of loops in JS
console.log("\n");
console.log("For loop");
for(let i = 0; i < 10; i++)
{
    console.log("i = " + i);
}


console.log("\n");
console.log("Do-while loop");
let j = 0
do {
    console.log("j = " + j);
    j++;
} while (j < 10);


console.log("\n");
console.log("While loop");
let k = 0
while (k < 10)
{
   console.log("k = " + k);
   k++;
}
